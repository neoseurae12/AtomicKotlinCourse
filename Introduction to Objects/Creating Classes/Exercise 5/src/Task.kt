// The Quick Documentation shortcut F1 can be used not only in the editor \
  // but also in the code completion popup list.

fun cap(s: String) = s.capitalize()

fun main() {
  println(cap("hi!"))
  println(cap("Hi!"))
}
/* Output:
Hi!
Hi!
 */